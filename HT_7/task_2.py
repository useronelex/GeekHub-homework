''' Запишіть в один рядок генератор списку (числа в діапазоні від 0 до 100 не включно), 
    сума цифр кожного елемент якого буде дорівнювати 10.
    Результат: [19, 28, 37, 46, 55, 64, 73, 82, 91]
'''

list_generator = [elem for elem in range(100) if elem % 10 + elem // 10 == 10]

print(list_generator)